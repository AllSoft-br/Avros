    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        char[] senha = pswSenha.getPassword();
        char[] nova = pswNova.getPassword();
        char[] conf = pswConf.getPassword();
        
        if((senha.length > 0) && (nova.length > 0) && (conf.length > 0)){
            if((Arrays.equals(nova, conf)) && (Arrays.equals(senha, FrmLogin.usuario.getSenha()))){
                
                try {
                    JDBCUpdate.usuarioSenha(pswNova.getPassword(), usuario.getId());
                    JOptionPane.showMessageDialog(null, "Senha atualizada com sucesso.");
                    this.dispose();
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(this, "A senha não pôde ser atualizada.", "Erro", JOptionPane.ERROR_MESSAGE);
                    Logger.getLogger(IfrmResetSenha.class.getName()).log(Level.SEVERE, null, ex);
                    return;
                }
                
            } else {
                JOptionPane.showMessageDialog(null, "Insira as informações corretamente.");
            } 
        } else {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos.");
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

